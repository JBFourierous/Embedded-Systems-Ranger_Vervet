/**
 * @brief  Classifies the input signal according to 4 classes, selecting with a maximum correlation logic
 * @param  input      points to input sequence
 * @return identifier of the input class recognized by the algorithm
 */
char classify(float32_t* input) {
	char id[3] = {'e','l','s'};
	float32_t tmp[INPUT_SIZE];
	float32_t corrs[3];
	const uint16_t highpass_size = HIGHPASS_SIZE;
	const uint16_t lms_size = LMS_SIZE;
	uint32_t start = 0, end = 0;
	start = HAL_GetTick();
	// Highpass filtering of the input signal
	filter(input, highpass_filter, highpass_size, input);
	// LMS filtering of the input signal for the three classes and correlation calculation
	corrs[0] = filter_and_correlate(input, w_lms_eagle, lms_size, tmp, eagle_df);
	corrs[1] = filter_and_correlate(input, w_lms_leo, lms_size, tmp, leo_df);
	corrs[2] = filter_and_correlate(input, w_lms_snk, lms_size, tmp, snk_df);
	// Ascending order
	if(fabsf(corrs[0]) > fabsf(corrs[2])) {
		swap_float(&corrs[0], &corrs[2]);
		swap_char(&id[0], &id[2]);
	}
	if(fabsf(corrs[0]) > fabsf(corrs[1])) {
		swap_float(&corrs[0], &corrs[1]);
		swap_char(&id[0], &id[1]);
	}
	if(fabsf(corrs[1]) > fabsf(corrs[2])) {
		swap_float(&corrs[1], &corrs[2]);
		swap_char(&id[1], &id[2]);
	}
	end = HAL_GetTick();
	// increase long classifications counter
	if(end-start > 600)
		long_classification_time++;
	// return the id corresponding to the maximum correlation
	if(fabsf(corrs[2]) > fabsf(2*corrs[1]))
		return id[2];
	else
		return 'n';
}

