/**
 * @brief  Swaps two input float values
 * @param  a      points to the first value
 * @param  b      points to the second value
 * @return none
 */
static void swap_float(float32_t* a, float32_t* b) {
	float32_t tmp;
	tmp = *b;
	*b = *a;
	*a = tmp;
}

/**
 * @brief  Swaps two input char values
 * @param  a      points to the first value
 * @param  b      points to the second value
 * @return none
 */
static void swap_char(char* a, char* b) {
	char tmp;
	tmp = *b;
	*b = *a;
	*a = tmp;
}

/**
 * @brief  FIR filtering of a sequence.
 * @param  input    points to the input sequence
 * @param  fir      points to the FIR tap weights
 * @param  fir_size FIR filter length
 * @return out      pointer to the filtered output
 */
static void filter(float32_t* input, const float32_t* fir, const uint16_t fir_size, float32_t* out) {
	arm_fir_instance_f32 S;
	float32_t fir_state[BLOCK_SIZE + fir_size -1];
	uint32_t block_size = BLOCK_SIZE;
	uint32_t num_blocks = INPUT_SIZE/BLOCK_SIZE;
	arm_fir_init_f32(&S, fir_size, fir, fir_state, block_size);
	for(int i=0; i < num_blocks; i++) {
		arm_fir_f32(&S, input+(i*block_size), out+(i*block_size), block_size);
	}
}

/**
 * @brief  Calculates the maximum of the correlation of two input sequences
 * @param  input  points to the first input sequence
 * @param  sample points to the second input sequence
 * @return max    maximum value of the correlation sequence
 */
static float32_t correlate(float32_t* input, const float32_t* sample) {
	float32_t max = -0.00001, xcorr = 0;
	bool backwards = false;
	int delay = 0, index = 0, limit = 0;
	for(delay = -INPUT_SIZE; delay < INPUT_SIZE; delay+=100) {
		if(delay < 0) {
			backwards = false;
			limit = INPUT_SIZE + delay;
		} else {
			backwards = true;
			limit = 5 - delay;
		}
		for(index = 0; index < limit; index++) {
			if(backwards) {
				xcorr = xcorr + input[index]*sample[delay+index];
			} else {
				xcorr = xcorr + input[-delay+index]*sample[index];
			}
		}
		if(xcorr > max)
			max = xcorr;
		xcorr = 0;
	}
	return max;
}

/** @brief  Filters an input sequence and evaluates its maximum correlation with a second input sequence after FIR filtering
 *  @param  input    pointer to the first input sequence
 *  @param  fir      pointer to the FIR tap weights
 *  @param  fir_size FIR filter length
 *  @param  out      pointer to the result of the FIR filtering
 *  @param  sample   pointer to the second input sequence
 *  @return maximum value of the correlation
 */
static float32_t filter_and_correlate(float32_t* input, const float32_t* fir, const uint16_t fir_size, float32_t* out, const float32_t* sample) {
	filter(input, fir, fir_size, out);
	return correlate(out, sample);
}

